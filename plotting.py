def plot_speed_time(skiers):
    """
    Plots speed vs. time for multiple skiers and annotates average and max speed.
    Also prints the time each skier takes to reach the bottom.
    """
    plt.figure(figsize=(10, 6))

    for k, skier in enumerate(skiers):
        traj = np.array(skier.traj)
        speeds = traj[:, 2]  # extract speed
        time = np.arange(len(speeds)) * skier.dt  # approximate time in seconds

        start_speed = speeds[0]
        avg_speed = np.mean(speeds)
        max_speed = np.max(speeds)

        # Compute finish time
        finish_indices = np.where(traj[:, 1] <= 0)[0]  # j=0 is bottom
        if len(finish_indices) > 0:
            finish_time = finish_indices[0] * skier.dt
        else:
            finish_time = None  # skier never reached bottom

        plt.plot(time, speeds, lw=1.5, alpha=0.7, label=f"Skier {k + 1}")

        if finish_time is not None:
            print(f"Skier {k + 1}: Level {skier.ability_level}, Start Speed = {start_speed:.2f} m/s, "
                  f"Avg Speed = {avg_speed:.2f} m/s, Max Speed = {max_speed:.2f} m/s, "
                  f"Finish Time = {finish_time:.2f} s")
        else:
            print(f"Skier {k + 1}: Level {skier.ability_level}, Start Speed = {start_speed:.2f} m/s, "
                  f"Avg Speed = {avg_speed:.2f} m/s, Max Speed = {max_speed:.2f} m/s, "
                  f"Finish Time = Did not finish")

    plt.xlabel("Time (s)")
    plt.ylabel("Speed (m/s)")
    plt.title("Skier Speed vs Time")
    plt.legend()
    plt.grid(True)
    plt.show()


def plot_space_time(skiers):
    """
    Space–time diagram: downhill position j vs time for each skier.
    """
    plt.figure(figsize=(10, 6))

    for k, skier in enumerate(skiers):
        traj = np.array(skier.traj)
        time = np.arange(len(traj))  # * skier.dt
        j_pos = traj[:, 1]  # downhill coordinate

        plt.plot(time[:30], j_pos[:30], lw=2, label=f"Skier {k + 1}")
        plt.scatter(time[:30], j_pos[:30], s=15, alpha=0.6)

    plt.xlabel("Steps")
    plt.ylabel("Downhill position j")
    plt.title("Space–Time Diagram (Downhill Position vs Time)")
    # plt.gca().invert_yaxis()  # optional: bottom of slope at bottom
    plt.legend()
    plt.grid(True)
    plt.show()


