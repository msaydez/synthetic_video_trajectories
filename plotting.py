def plot_envelope_and_trajectories(
    gt_trajs, gen_traj, y_centers, mean_x, std_x, valid_mask, k=2
):
    plt.figure(figsize=(6, 8))

    # Plot GT trajectories
    for traj in gt_trajs:
        plt.plot(traj[:, 0], traj[:, 1], color='gray', alpha=0.3)

    # Envelope
    y = y_centers[valid_mask]
    mx = mean_x[valid_mask]
    sx = std_x[valid_mask]

    plt.plot(mx, y, 'b', label='GT mean')
    plt.fill_betweenx(
        y, mx - k*sx, mx + k*sx,
        color='blue', alpha=0.25, label=f'±{k}σ'
    )

    # Generated
    plt.plot(gen_traj[:, 0], gen_traj[:, 1],
             'r', linewidth=2, label='Generated')

    plt.gca().invert_yaxis()
    plt.xlabel("x (image px)")
    plt.ylabel("y (image px)")
    plt.legend()
    plt.title("GT trajectory envelope vs generated")
    plt.grid(True)
    plt.show()

def plot_skiers_trajectories(skiers, M=100, J=200, sigma=3):

    corners = np.array([
    [0, 0],    # bottom-left
    [M, 0],    # bottom-right
    [M, J],    # top-right
    [0, J],    # top-left
    [0, 0]     # close loop
    ])

    for skier in skiers:
        kappa= skier.kappa
        break

    plt.figure(figsize=(10, 6))
    plt.plot(corners[:,0], corners[:,1], "b--", lw=2, label="Automaton boundary")
    im = plt.imshow(kappa.T, origin='lower', cmap='RdBu', extent=[0, skier.M, 0, skier.J])
    plt.colorbar(im, label='Curvature')
    for k, s in enumerate(skiers):
        traj = np.array(s.traj)
        plt.plot(traj[:,0], traj[:,1], lw=1, color="black", alpha=0.35)
        i_s, j_s = smooth_traj(traj[:,0], traj[:,1], sigma=sigma)
        plt.plot(i_s, j_s, lw=3,label=f"Skier {k+1}")
    #plt.gca().invert_yaxis()
    plt.xlim(0, M)
    plt.ylim(0, J)
    plt.axis("equal")
    plt.xlabel("i (lateral)")
    plt.ylabel("j (downhill)")
    plt.title("Synchronous Multi-Skier Trajectories")
    plt.legend()
    plt.grid(True)
    plt.show()

