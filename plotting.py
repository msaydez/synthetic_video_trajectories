def plot_space_time_2d(skiers):
    """
    2D space–time plot: (i, j) positions colored by time.
    """
    plt.figure(figsize=(8, 10))

    for k, skier in enumerate(skiers):
        traj = np.array(skier.traj)
        time = np.arange(len(traj))  # * skier.dt

        sc = plt.scatter(
            traj[:, 0],  # i
            traj[:, 1],  # j
            c=time,
            cmap="viridis",
            s=12,
            alpha=0.8,
            label=f"Skier {k + 1}"
        )

    plt.colorbar(sc, label="Time (s)")
    plt.xlabel("i (lateral)")
    plt.ylabel("j (downhill)")
    plt.title("2D Space–Time Trajectories (Color = Time)")
    plt.xlim(0, skiers[0].M)
    plt.ylim(0, skiers[0].J)
    plt.grid(True)
    plt.show()

def plot_bboxes_with_trajectories(all_bboxes, proj_trajs, img_w=1280, img_h=720, max_frames=3000):
    """
    Plot projected trajectories with bounding boxes
    """
    plt.figure(figsize=(10,6))
    colors = plt.cm.tab20(np.linspace(0,1,len(all_bboxes)))

    for k, (bboxes, traj) in enumerate(zip(all_bboxes, proj_trajs)):
        T = min(len(bboxes), max_frames)
        #print(f"Skier {k+1}")
        for t in range(T):
            cx, cy, w, h = bboxes[t]
            rect = plt.Rectangle(
                (cx - w/2, cy - h/2), w, h,
                fill=False, linewidth=1, alpha=0.5, edgecolor=colors[k]
            )
            plt.gca().add_patch(rect)
            #print(cx - w/2, cy - h, w, h)

        # Plot smoothed trajectory
        plt.plot(traj[:T,0], traj[:T,1], color=colors[k], lw=2, label=f"Skier {k+1}")

    plt.xlim(0, img_w)
    plt.ylim(img_h, 0)  # image coordinates: top=0
    plt.xlabel("x (pixels)")
    plt.ylabel("y (pixels)")
    plt.title("Projected Skier Trajectories with Bounding Boxes")
    plt.grid(True)
    #plt.legend()
    plt.show()


def save_frames_and_video(skiers, filename="skiers_short_w_rhythm.mp4", steps=1000):
    """
    Save a short video of the first `steps` simulation steps,
    respecting each skier's staggered start.
    """
    frames = []
    colors = plt.cm.tab20(np.linspace(0, 1, len(skiers)))

    for frame in range(steps):
        fig, ax = plt.subplots(figsize=(10, 6))
        ax.set_xlim(0, skiers[0].M)
        ax.set_ylim(0, skiers[0].J)
        ax.set_xlabel("i (lateral)")
        ax.set_ylabel("j (downhill)")
        ax.set_aspect("equal", adjustable="box")
        ax.set_title(f"Skiers Simulation - Step {frame + 1}")
        ax.grid(True)

        for k, skier in enumerate(skiers):
            # Only consider skier if they have started
            if frame >= skier.start_step:
                traj = np.array(skier.traj)
                i_s, j_s = smooth_traj(traj[:,0], traj[:,1], sigma=9)
                #i_s, j_s = traj[:, 0], traj[:, 1]
                traj_smooth = np.stack((i_s, j_s), axis=1)

                # Clip trajectory to current frame
                last_frame_idx = min(frame - skier.start_step + 1, len(traj_smooth))
                i_pos = traj_smooth[:last_frame_idx, 0]
                j_pos = traj_smooth[:last_frame_idx, 1]

                ax.plot(i_pos, j_pos, color=colors[k], lw=2, alpha=0.7, label=f"Skier {k + 1}, Level{skier.aggressiveness}")
                ax.scatter(i_pos[-1:], j_pos[-1:], color=colors[k], s=50)

        # Draw the frame to a numpy array
        #ax.legend()
        fig.canvas.draw()
        image = np.frombuffer(fig.canvas.tostring_rgb(), dtype='uint8')
        image = image.reshape(fig.canvas.get_width_height()[::-1] + (3,))
        frames.append(image)
        plt.close(fig)


    # Save video using imageio
    imageio.mimwrite(filename, frames, fps=30)  # lower fps for clarity
    print(f"Short simulation video saved as {filename}")







